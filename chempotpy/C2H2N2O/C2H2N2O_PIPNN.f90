      subroutine pes(x,igrad,path,p,g,d)

      use nnparam1
      implicit none
      ! number of electronic state
      integer, parameter :: nstates=1
      integer, parameter :: natoms=7
      integer, intent(in) :: igrad
      character(len=1024), intent(in) :: path
      double precision, intent(in) :: x(natoms,3)
      double precision, intent(out) :: p(nstates), g(nstates,natoms,3)
      double precision, intent(out) :: d(nstates,nstates,natoms,3)


      double precision :: v, tx(3,natoms)
      integer :: iatom, idir, j, istate
      !initialize 
      v=0.d0
      g=0.d0
      d=0.d0

      do iatom=1,natoms
        do idir=1,3
          tx(idir, iatom)=x(iatom, idir)
        enddo
      enddo

      if (igrad==0) then
        call pes_init(path)
        call evvdvdx(tx,v)
        deallocate(nodes3a1)
        deallocate(weight3a1)
        deallocate(bias3a1)
        deallocate(pdel3a1)
        deallocate(pavg3a1)
      else
        write (*,*) 'Only energy is available'
      endif

      do istate=1,nstates
        p(istate)=v
      enddo

      endsubroutine



      subroutine evvdvdx(xcart,v)
!**************************************************************************!
!Subroutine to calculate the potential energy
!Xcart     ==>Cartesian coordinates in angstrom
!             H1: xcart(1,1), xcart(2,1), xcart(3,1)
!             H2: xcart(1,2), xcart(2,2), xcart(3,2)
!             C3: xcart(1,3), xcart(2,3), xcart(3,3)
!             C4: xcart(1,4), xcart(2,4), xcart(3,4)
!             N5: xcart(1,5), xcart(2,5), xcart(3,5)
!             N6: xcart(1,6), xcart(2,6), xcart(3,6)
!             N7: xcart(1,7), xcart(2,7), xcart(3,7)
!v         ==>Average potential energy(in eV)
!The total root mean squared error is 16.0 meV
!It's worth nothing that the trajectories should be artificially halted
!when the two forming bond distances C-N and C-O arrived at 1.6 angstrom, 
!otherwise, the product might decompose.
!“Quantitative Dynamics of the N2O + C2H2 → Oxadiazole Reaction: 
!A Model for 1,3-Dipolar Cycloadditions”, 
!Yang Liu, Jun Li*, ACS Omega, 2020, 5, 23343-23350.
!https://pubs.acs.org/doi/10.1021/acsomega.0c03210
!***************************************************************************
      implicit none
      real*8,parameter::alpha=1.8d0,PARA=0.5291772d0
      integer,parameter::natom=7
      integer i,j,k,ndriv
      real*8 v,p(0:2389),r(1:21)
      real*8 xcart(1:3,1:7),m(0:707),xmorse(1:21)
      real*8 x(1:21)
      real*8 txinput(1:456)

      do i=1,natom
       x((3*(i-1)+1):(3*(i-1)+3))=xcart(:,i)
      enddo

      k=0
      do i=1,natom-1
       do j=i+1,natom
        k=k+1
       r(k)=dsqrt(dot_product(xcart(:,i)-xcart(:,j),
     $                  xcart(:,i)-xcart(:,j)))
        xmorse(k)=dexp(-r(k)/(PARA*alpha))
       enddo
      enddo
      if(k.ne.(natom*(natom-1)/2))stop "error"
      call bemsav(xmorse,p)

      do j=1,456
       txinput(j)=p(j)
      enddo

      call pot3a(txinput,v)

      return
      end subroutine evvdvdx

      subroutine pes_init(path)
      use nnparam1
      implicit none
      character(len=1024), intent(in) :: path
      integer i,ihid,iwe,inode1,inode2,ilay1,ilay2
      integer ibasis,npd,iterm,ib,nfile1,nfile2
      character f1*80
      character(len=1024) :: file_path1, file_path2

      nfile1=4
      nfile2=7

      file_path1 = trim(path)//"/C2H2N2O/weights.txt"
      file_path2 = trim(path)//"/C2H2N2O/biases.txt"

      open(nfile1,file=file_path1,status='old')
      open(nfile2,file=file_path2,status='old')
      read(nfile1,*)ninput1,nhid3,noutput1
      nscale1=ninput1+noutput1
      nlayer31=nhid3+2 !additional one for input layer and one for output 
      allocate(nodes3a1(nlayer31),pdel3a1(nscale1),pavg3a1(nscale1))
      nodes3a1(1)=ninput1
      nodes3a1(nlayer31)=noutput1
      read(nfile1,*)(nodes3a1(ihid),ihid=2,nhid3+1)
      nodemax31=0
      do i=1,nlayer31
       nodemax31=max(nodemax31,nodes3a1(i))
      enddo
      allocate(weight3a1(nodemax31,nodemax31,2:nlayer31),
     $bias3a1(nodemax31,2:nlayer31))
      read(nfile1,*)ifunc31,nwe3
      read(nfile1,*)(pdel3a1(i),i=1,nscale1)
      read(nfile1,*)(pavg3a1(i),i=1,nscale1)
      iwe=0
      do ilay1=2,nlayer31
      ilay2=ilay1-1
      do inode1=1,nodes3a1(ilay1)
      do inode2=1,nodes3a1(ilay2) !
      read(nfile1,*)weight3a1(inode2,inode1,ilay1)
      iwe=iwe+1
      enddo
      read(nfile2,*)bias3a1(inode1,ilay1)
      iwe=iwe+1
      enddo
      enddo
      if (iwe.ne.nwe3) then
        write(*,*)'provided number of parameters ',nwe3
        write(*,*)'actual number of parameters ',iwe
        write(*,*)'nwe not equal to iwe, check input files or code'
        stop
      endif

      close(nfile1)
      close(nfile2)

      end subroutine pes_init

